def matvec1(a,x):
    """
    ***************************************************************************
    * DATA DE CRIACAO  : 20/02/2019                                           *
    * DATA DE MODIFICAO: 00/00/0000                                           *
    * ----------------------------------------------------------------------- *
    * matvec1: produto matriz vetor                                           *
    * ----------------------------------------------------------------------- *
    * Parametros de entrada:                                                  *
    * ----------------------------------------------------------------------- *
    * a - matriz de coefientes n x n                                          *
    * x - vetor x                                                             *
    * ----------------------------------------------------------------------- *
    * Parametros de saida:                                                    *
    * ----------------------------------------------------------------------- *
    * y - retorna o vetor y = a*x                                             *
    * ----------------------------------------------------------------------- *
    * OBS:                                                                    *
    * ----------------------------------------------------------------------- *
    ***************************************************************************
    """
    nl = len(x)
    nc = len(a[0:])
    y  = nc*[0.0]
    for i in range(0,nl):
        for j in range(0,nc):
            y[i] += a[i][j]*x[j]

    return y
