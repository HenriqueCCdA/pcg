def dot1(x,y):
    """
    ***************************************************************************
    * DATA DE CRIACAO  : 20/02/2019                                           *
    * DATA DE MODIFICAO: 00/00/0000                                           *
    * ----------------------------------------------------------------------- *
    * dot1: produto interno                                                   *
    * ----------------------------------------------------------------------- *
    * Parametros de entrada:                                                  *
    * ----------------------------------------------------------------------- *
    * x - vetor x                                                             *
    * y - vetor y                                                             *
    * ----------------------------------------------------------------------- *
    * Parametros de saida:                                                    *
    * ----------------------------------------------------------------------- *
    * dot - retorna o produto interno x*y                                     *
    * ----------------------------------------------------------------------- *
    * OBS:                                                                    *
    * ----------------------------------------------------------------------- *
    ***************************************************************************
    """
    dot = 0.0
    for xi,yi in zip(x,y):
        dot += xi*yi

    return dot
