def fb_ilu(r, m, neq: int):
    """
    ***************************************************************************
    * DATA DE CRIACAO  : 26/09/2020                                           *
    * DATA DE MODIFICAO: 00/00/0000                                           *
    * ----------------------------------------------------------------------- *
    * fb_ilu: forward e backward substituicao                                 *
    * ----------------------------------------------------------------------- *
    * Parametros de entrada:                                                  *
    * ----------------------------------------------------------------------- *
    * r   - vetor de indepemdente n x 1                                       *
    * m   - matriz de coeficientes n x n                                      *
    * neq - numero de equacoes                                                *
    * ----------------------------------------------------------------------- *
    * Parametros de saida:                                                    *
    * ----------------------------------------------------------------------- *
    * y    - retorna a solução                                                *
    * ----------------------------------------------------------------------- *
    * OBS:                                                                    *
    * ----------------------------------------------------------------------- *
    * y = M(-1) r                                                             *
    ***************************************************************************
    """

    t = r.copy()

    for i in range(0, neq):
        for j in range(0, i):
            t[i, 0] -= m[i, j] * t[j, 0]

    for i in range(neq-1, -1, -1):
        t[i, 0] = t[i, 0]/m[i, i]
        for j in range(i + 1, neq):
            t[i, 0] -= m[i, j] * t[j, 0] / m[i, i]

    return t
