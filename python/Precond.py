import numpy as np

# *****************************************************************************
def ilu(a, neq: int):
    """
    ***************************************************************************
    * DATA DE CRIACAO  : 26/09/2020                                           *
    * DATA DE MODIFICAO: 00/00/0000                                           *
    * ----------------------------------------------------------------------- *
    * dilu: monta o precondionador DILU                                       *
    * ----------------------------------------------------------------------- *
    * Parametros de entrada:                                                  *
    * ----------------------------------------------------------------------- *
    * a   - matriz de coeficientes n x n                                      *
    * neq - numero de equacoes                                                *
    * ----------------------------------------------------------------------- *
    * Parametros de saida:                                                    *
    * ----------------------------------------------------------------------- *
    * m    - retrona o precondicionador n x 1                                 *
    * ----------------------------------------------------------------------- *
    * OBS:                                                                    *
    * ----------------------------------------------------------------------- *
    *                                                                         *
    ***************************************************************************
    """

    m = np.zeros((neq,neq),dtype=float)

    m = a.copy()
    for k in range(0, neq-1):
        for i in range(k + 1, neq):
            if m[i, k] != 0.0:
                m[i, k] = m[i, k] / m[k, k]

            for j in range(k + 1, neq):
                if m[i, j] != 0.0:
                    m[i, j] = m[i, j] - m[i, k] * m[k, j]

    return m
